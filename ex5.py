# 5. Verifica si afiseaza daca diferenta dintre x si y este mai mica decat 5.

x = int(input('Introduceti primul nr : '))
y = int(input('Introduceti al doilea nr : '))
if ( x-y < 5 ):
    print ('Diferenta celor doua nr este mai mica decat 5 !')
else:
    print ('Diferenta celor doua nr nu este mai mica decat 5. Reintroduceti alte nr!')